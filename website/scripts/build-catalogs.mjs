#!/usr/bin/env node
/**
 * build-catalogs.mjs
 *
 * Reads:
 *   - ../registry/sources.yaml
 *   - ../skills/<name>/SKILL.md (frontmatter only)
 *
 * Writes:
 *   - src/content/docs/skills/index.md
 *   - src/content/docs/sources/index.md
 *
 * Idempotent. Run before `astro dev` / `astro build`.
 *
 * Zero npm dependencies — hand-rolled YAML subset parser sufficient for our
 * registry format (top-level array of objects, primitive scalars, simple
 * inline glob lists) and skill frontmatter (flat scalars, folded `>-`).
 */

import { readFileSync, writeFileSync, readdirSync, statSync, mkdirSync, existsSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const WEBSITE_ROOT = resolve(__dirname, '..');
const REPO_ROOT = resolve(WEBSITE_ROOT, '..');
const REGISTRY = join(REPO_ROOT, 'registry', 'sources.yaml');
const SKILLS_DIR = join(REPO_ROOT, 'skills');
const REPO_URL = 'https://github.com/cardano-foundation/cardano-dev-skills';
const REPO_TREE = `${REPO_URL}/tree/main`;

const SKILLS_OUT = join(WEBSITE_ROOT, 'src', 'content', 'docs', 'skills', 'index.md');
const SOURCES_OUT = join(WEBSITE_ROOT, 'src', 'content', 'docs', 'sources', 'index.md');

const HEADER = '<!-- AUTO-GENERATED by scripts/build-catalogs.mjs — do not edit by hand -->';

// ---------------------------------------------------------------------------
// Minimal YAML parsers (tailored to our two consumers).
// ---------------------------------------------------------------------------

function stripQuotes(s) {
  s = s.trim();
  if (s.length >= 2 && ((s.startsWith('"') && s.endsWith('"')) || (s.startsWith("'") && s.endsWith("'")))) {
    return s.slice(1, -1);
  }
  return s;
}

function parseScalar(v) {
  v = stripQuotes(v.trim());
  if (v === 'true') return true;
  if (v === 'false') return false;
  if (v === 'null' || v === '~' || v === '') return null;
  return v;
}

/**
 * Parse `registry/sources.yaml`: a top-level YAML sequence of mappings.
 * Returns an array of plain objects. Comments and blank lines ignored.
 * Supports nested mappings (e.g. `format_overrides:`) and inline / block
 * sequences (e.g. `glob_patterns:`).
 */
function parseSourcesYaml(raw) {
  const lines = raw.split(/\r?\n/);
  // strip comments, then a few helper indices
  const items = [];
  let i = 0;
  const stripComment = (line) => {
    // naive: only strip `#` that isn't inside quotes. None of our values use `#`.
    const idx = line.indexOf('#');
    if (idx === -1) return line;
    // keep `#` if preceded by quotes balance — but our YAML doesn't have inline `#`-after-value, so simple strip is fine
    return line.slice(0, idx);
  };
  const isBlank = (line) => /^\s*$/.test(line);

  while (i < lines.length) {
    let line = stripComment(lines[i]);
    if (isBlank(line)) {
      i++;
      continue;
    }
    const m = line.match(/^- (.*)$/);
    if (!m) {
      i++;
      continue;
    }
    // Start of a new item. The first kv pair is on this line.
    const item = {};
    const firstKv = m[1];
    const baseIndent = line.indexOf('- ');
    const childIndent = baseIndent + 2;

    parseKv(firstKv, item, lines, i, childIndent, (newIdx) => { i = newIdx; });
    i++;

    // Subsequent lines belong to this item until we see another `- ` at baseIndent or shallower
    while (i < lines.length) {
      const next = stripComment(lines[i]);
      if (isBlank(next)) { i++; continue; }
      const indent = next.match(/^( *)/)[1].length;
      if (indent < childIndent) break;
      // strip leading childIndent spaces
      const trimmed = next.slice(childIndent);
      const consumed = parseBlock(trimmed, item, lines, i, childIndent);
      i = consumed;
    }
    items.push(item);
  }
  return items;
}

/**
 * Parse one `key: value` line into `target`. If value is empty, peek ahead
 * for a nested mapping/sequence and consume it.
 *
 * Returns the index of the last consumed line. (For our use, this helper is
 * only called for inline single-line scalars on the `- ` opener.)
 */
function parseKv(text, target, lines, i, childIndent, setIdx) {
  const m = text.match(/^([A-Za-z_][A-Za-z0-9_]*): ?(.*)$/);
  if (!m) return i;
  const key = m[1];
  const val = m[2];
  if (val === '' || val === undefined) {
    target[key] = null; // placeholder; nested parser may overwrite
  } else {
    target[key] = parseScalar(val);
  }
}

/**
 * Parse a single block-form line that belongs to the current item, possibly
 * consuming nested lines. Returns the next line index to read.
 */
function parseBlock(text, target, lines, i, childIndent) {
  // text already has childIndent stripped
  const m = text.match(/^([A-Za-z_][A-Za-z0-9_]*): ?(.*)$/);
  if (!m) return i + 1;
  const key = m[1];
  const val = m[2];

  if (val === '' || val === undefined) {
    // Look ahead — could be either a nested mapping or a sequence
    let j = i + 1;
    // skip blank/comment
    while (j < lines.length && /^\s*(#|$)/.test(lines[j])) j++;
    if (j >= lines.length) {
      target[key] = null;
      return j;
    }
    const peek = lines[j];
    const peekIndent = peek.match(/^( *)/)[1].length;
    if (peekIndent <= childIndent) {
      target[key] = null;
      return i + 1;
    }
    // It's nested. Sequence if first non-blank starts with `- `
    const peekContent = peek.slice(peekIndent);
    if (peekContent.startsWith('- ')) {
      const seq = [];
      const seqIndent = peekIndent;
      let k = j;
      while (k < lines.length) {
        const line = lines[k];
        if (/^\s*(#|$)/.test(line)) { k++; continue; }
        const ind = line.match(/^( *)/)[1].length;
        if (ind < seqIndent) break;
        const content = line.slice(ind);
        if (!content.startsWith('- ')) break;
        seq.push(parseScalar(content.slice(2)));
        k++;
      }
      target[key] = seq;
      return k;
    } else {
      // nested mapping
      const sub = {};
      const subIndent = peekIndent;
      let k = j;
      while (k < lines.length) {
        const line = lines[k];
        if (/^\s*(#|$)/.test(line)) { k++; continue; }
        const ind = line.match(/^( *)/)[1].length;
        if (ind < subIndent) break;
        const content = line.slice(ind);
        const sm = content.match(/^([^:]+): ?(.*)$/);
        if (sm) sub[stripQuotes(sm[1])] = parseScalar(sm[2]);
        k++;
      }
      target[key] = sub;
      return k;
    }
  }
  target[key] = parseScalar(val);
  return i + 1;
}

/**
 * Parse YAML frontmatter from a SKILL.md. Supports plain scalars and `>-`
 * folded multi-line strings.
 */
function parseSkillFrontmatter(md) {
  const m = md.match(/^---\n([\s\S]*?)\n---/);
  if (!m) return {};
  const block = m[1];
  const lines = block.split(/\r?\n/);
  const out = {};
  let i = 0;
  while (i < lines.length) {
    const line = lines[i];
    const kv = line.match(/^([A-Za-z_][A-Za-z0-9_-]*): ?(.*)$/);
    if (!kv) { i++; continue; }
    const key = kv[1];
    let val = kv[2];
    if (val === '>-' || val === '>') {
      // folded multi-line — collect indented continuation lines, joined with spaces
      const parts = [];
      let j = i + 1;
      while (j < lines.length && /^\s+/.test(lines[j])) {
        parts.push(lines[j].trim());
        j++;
      }
      out[key] = parts.join(' ').replace(/\s+/g, ' ').trim();
      i = j;
      continue;
    }
    if (val === '|-' || val === '|') {
      const parts = [];
      let j = i + 1;
      while (j < lines.length && /^\s+/.test(lines[j])) {
        parts.push(lines[j].trim());
        j++;
      }
      out[key] = parts.join('\n');
      i = j;
      continue;
    }
    out[key] = stripQuotes(val);
    i++;
  }
  return out;
}

// ---------------------------------------------------------------------------
// Catalog generation
// ---------------------------------------------------------------------------

function loadSkills() {
  const skills = [];
  const entries = readdirSync(SKILLS_DIR);
  for (const name of entries.sort()) {
    const dir = join(SKILLS_DIR, name);
    if (!statSync(dir).isDirectory()) continue;
    const skillFile = join(dir, 'SKILL.md');
    if (!existsSync(skillFile)) continue;
    const content = readFileSync(skillFile, 'utf8');
    const fm = parseSkillFrontmatter(content);
    if (!fm.name) continue;
    skills.push({
      name: fm.name,
      dir: name,
      description: (fm.description || '').replace(/\s+/g, ' ').trim(),
    });
  }
  return skills;
}

function loadSources() {
  const raw = readFileSync(REGISTRY, 'utf8');
  return parseSourcesYaml(raw).filter((s) => s && s.name && s.repo);
}

function escapeMd(s) {
  return String(s).replace(/\|/g, '\\|').replace(/\n+/g, ' ').trim();
}

function repoLabelFromUrl(repo) {
  return repo.replace(/^https?:\/\/(www\.)?github\.com\//, '').replace(/\.git$/, '');
}

function ensureDir(file) {
  mkdirSync(dirname(file), { recursive: true });
}

function buildSkillsPage(skills) {
  const lines = [];
  lines.push('---');
  lines.push('title: Skills');
  lines.push(`description: ${JSON.stringify(`${skills.length} developer skills covering common Cardano workflows.`)}`);
  lines.push('---');
  lines.push('');
  lines.push(HEADER);
  lines.push('');
  lines.push(
    `${skills.length} skills, listed alphabetically. Each is a standalone Markdown file under ` +
      '[`skills/`](' + REPO_TREE + '/skills/) in the repo. The agent loads only ' +
      '`name` + `description` at session start; the full SKILL.md loads when ' +
      'the skill is invoked.',
  );
  lines.push('');
  lines.push('| Skill | What it does |');
  lines.push('| --- | --- |');
  for (const s of skills) {
    const link = `${REPO_TREE}/skills/${s.dir}/SKILL.md`;
    const safeDesc = escapeMd(s.description);
    lines.push(`| [\`${s.name}\`](${link}) | ${safeDesc} |`);
  }
  lines.push('');
  lines.push('## How to invoke');
  lines.push('');
  lines.push(
    'In Claude Code, prefix the skill name with the plugin: ' +
      '`cardano-dev-skills:write-validator`. Or describe the task in natural ' +
      'language — skills carry trigger phrases that auto-match. See ' +
      '[How it works](/cardano-dev-skills/how-it-works/) for the matching ' +
      'mechanism and what to do when it misses.',
  );
  lines.push('');
  return lines.join('\n');
}

function categoryLabel(cat) {
  if (!cat) return 'uncategorised';
  return cat;
}

function buildSourcesPage(sources) {
  const byCat = new Map();
  for (const s of sources) {
    const cat = categoryLabel(s.category);
    if (!byCat.has(cat)) byCat.set(cat, []);
    byCat.get(cat).push(s);
  }
  const catOrder = [
    'infrastructure',
    'smart-contracts',
    'sdk',
    'testing',
    'governance',
    'scaling',
    'oracles',
    'standards',
  ];
  const cats = [...byCat.keys()].sort((a, b) => {
    const ai = catOrder.indexOf(a);
    const bi = catOrder.indexOf(b);
    if (ai === -1 && bi === -1) return a.localeCompare(b);
    if (ai === -1) return 1;
    if (bi === -1) return -1;
    return ai - bi;
  });

  const lines = [];
  lines.push('---');
  lines.push('title: Sources');
  lines.push(
    `description: ${JSON.stringify(`${sources.length} Cardano documentation sources mirrored locally and auto-refreshed weekly.`)}`,
  );
  lines.push('---');
  lines.push('');
  lines.push(HEADER);
  lines.push('');
  lines.push(
    `${sources.length} upstream Cardano projects mirrored under ` +
      '[`docs/sources/`](' + REPO_TREE + '/docs/sources/). Auto-refreshed ' +
      'every Monday at 06:00 UTC by [`refresh-docs.yml`](' + REPO_TREE +
      '/.github/workflows/refresh-docs.yml). The canonical entry for each is ' +
      '[`registry/sources.yaml`](' + REPO_TREE + '/registry/sources.yaml).',
  );
  lines.push('');
  lines.push(
    'Sources are grouped by category. Priority (`high` / `medium` / `low`) ' +
      'is shown next to each entry — it reflects the maintainer\'s judgement ' +
      'of how important the source is to the typical Cardano dev workflow.',
  );
  lines.push('');

  for (const cat of cats) {
    const items = byCat.get(cat).slice().sort((a, b) => a.name.localeCompare(b.name));
    lines.push(`## ${cat}`);
    lines.push('');
    lines.push('| Source | Priority | Description |');
    lines.push('| --- | --- | --- |');
    for (const s of items) {
      const repoLabel = repoLabelFromUrl(s.repo);
      const desc = escapeMd(s.description || '');
      const website = s.website ? ` · [site](${s.website})` : '';
      const prio = s.priority || '—';
      lines.push(
        `| **${escapeMd(s.name)}** — [\`${repoLabel}\`](${s.repo.replace(/\.git$/, '')})${website} | ${prio} | ${desc} |`,
      );
    }
    lines.push('');
  }

  lines.push('## Maintenance bar');
  lines.push('');
  lines.push(
    'Every entry must satisfy: last commit < 6 months old; ≥1 release tag ' +
      'or active issue/PR activity in the last 3 months; no archived / ' +
      'deprecated / sunset banner; for forks, the maintained canonical. See ' +
      '[Governance](/cardano-dev-skills/contributing/governance/) for the ' +
      'full policy.',
  );
  lines.push('');
  return lines.join('\n');
}

function main() {
  console.log('[build-catalogs] reading registry + skills…');
  const skills = loadSkills();
  const sources = loadSources();
  console.log(`[build-catalogs] ${skills.length} skills, ${sources.length} sources`);

  ensureDir(SKILLS_OUT);
  ensureDir(SOURCES_OUT);

  writeFileSync(SKILLS_OUT, buildSkillsPage(skills));
  writeFileSync(SOURCES_OUT, buildSourcesPage(sources));

  console.log(`[build-catalogs] wrote ${SKILLS_OUT}`);
  console.log(`[build-catalogs] wrote ${SOURCES_OUT}`);
}

main();
